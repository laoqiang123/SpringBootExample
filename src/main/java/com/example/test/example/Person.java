package com.example.test.example;

import org.springframework.beans.factory.annotation.Value;

public class Person {
	@Value("jxl")
	private String name;
	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
